package javapackage;

public class StringDemo3 {

	public static void main(String[] args) {
		
		String name1= "computer";
		String name2= "		AutomationTesting      ";
		String name3= "WELCOME";
		String name4= "java programming";
		
		String convert1 = name1.toLowerCase();
		String convert2 = name2.toLowerCase();
		String convert3 = name3.toLowerCase();
		String convert4 = name4.toLowerCase();
		
		System.out.println("LowerCase : "+convert1);
		System.out.println("LowerCase : "+convert2);
		System.out.println("LowerCase : "+convert3);
		System.out.println("LowerCase : "+convert4);
		

	}

}
