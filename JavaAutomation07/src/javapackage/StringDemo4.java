package javapackage;

public class StringDemo4 {

	public static void main(String[] args) {
		
		String name1= "computer";
		String name2= "		AutomationTesting      ";
		String name3= "WELCOME";
		String name4= "java programming";
		
		String convert1 = name1.toUpperCase();
		String convert2 = name2.toUpperCase();
		String convert3 = name3.toUpperCase();
		String convert4 = name4.toUpperCase();
		
		System.out.println("UpperCase : "+convert1);
		System.out.println("UpperCase : "+convert2);
		System.out.println("UpperCase : "+convert3);
		System.out.println("UpperCase : "+convert4);

	}

}
